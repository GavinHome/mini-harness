"""
ANSI 终端颜色转义码定义
用于控制台输出的彩色格式化
"""

# 前景色
CYAN = '\033[96m'      # 青色 - 思考内容
GREEN = '\033[92m'     # 绿色 - 最终回复
YELLOW = '\033[93m'    # 黄色 - Token 统计、工具调用
GRAY = '\033[90m'      # 灰色 - 分隔符
MAGENTA = '\033[95m'   # 品红色 - 调试信息
RED = '\033[91m'       # 红色 - 错误信息
BLUE = '\033[94m'      # 蓝色 - 信息提示

# 重置
RESET = '\033[0m'      # 重置所有样式
BOLD = '\033[1m'       # 粗体

# 背景色（可选）
BG_GRAY = '\033[47m'   # 灰色背景